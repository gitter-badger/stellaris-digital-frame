void 
ssd1289_box(color_t color, yaxis_t y, xaxis_t x, yaxis_t height, xaxis_t width)
{
	xaxis_t tx;
	yaxis_t ty;
	
	for(tx = x; tx <= x + width; tx++)
	{
		if(!(tx < SSD1289_WIDTH))
		{
			break;
		}
		
		for(ty = y; ty <= y + height; ty++)
		{
			if(!(ty < SSD1289_HEIGHT))
			{
				break;
			}
			
			ssd1289_pixel(color, ty, tx);
		}
	}
}

void
ssd1289_rectangle(color_t color, yaxis_t y, xaxis_t x, yaxis_t height, xaxis_t width)
{
	/* TODO: use a window HSA in the datasheet */
	
	ssd1289_hline(color, y, x, width);
	ssd1289_vline(color, y, x, height);
	
	ssd1289_hline(color, y+height, x, width);
	ssd1289_vline(color, y, x+width, height);
}

void
ssd1289_hline(color_t color, yaxis_t y, xaxis_t x, xaxis_t width)
{
	xaxis_t tx;
	
	for(tx = x; tx <= x + width; tx++)
	{
		ssd1289_pixel(color, y, tx);
	}
}

void
ssd1289_vline(color_t color, yaxis_t y, xaxis_t x, yaxis_t height)
{
	yaxis_t ty;
	
	for(ty = y; ty <= y + height; ty++)
	{
		ssd1289_pixel(color, ty, x);
	}
}

void
ssd1289_line(color_t color, yaxis_t start_y, xaxis_t start_x, 
							yaxis_t end_y, 	xaxis_t end_x)
{
	/* dy = dx . m */
	/* m =(y - y`)/(x - x`) */
	
	float slope =(end_y - start_y)/(end_x - start_x);
	xaxis_t tx;
	
	for(tx = start_x; tx <= end_x; tx++)
	{
		ssd1289_pixel(color, start_y + (tx * slope), tx);
	}
}

void 
ssd1289_char(color_t color, char ch, yaxis_t y, xaxis_t x)
{
	if((x + 8) > SSD1289_WIDTH ||(y + 16) > SSD1289_HEIGHT)
	{
		return;
	}
	
	ch -= 32;
	
	if(!(ch < 95))
	{
		/* overflow */
		return;
	}
	
	char *font = (char *)font_ascii[ch];
	
	uint8_t i, j;
	uint8_t fch;
	
	for(i = 0 ; !(i > 16); i++)
	{
		fch = font[i];
		
		j = 8;
		while(--j)
		{
			if(fch & 0x01)
			{
				ssd1289_pixel(color, y + i, x + j);
			}
			
			fch >>= 1;
		}
	}
}

void
ssd1289_string(color_t color, char *string, yaxis_t y, xaxis_t x)
{
	yaxis_t tx;
	
	for(tx = x; *string; tx += 8, string++)
	{
		ssd1289_char(color, *string, y, tx);
	}
}


