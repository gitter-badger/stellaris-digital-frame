void
ssd1289_set_cursor(yaxis_t y, xaxis_t x)
{
    ssd1289_send(0x004e, y);
    ssd1289_send(0x004f, SSD1289_WIDTH - 1 - x);
}


void WriteLow(uint8_t d)
{
	GPIOPinWrite(LCD_DATAL_BASE, 0xFF, d);
}

void WriteHigh(uint8_t d)
{
	GPIOPinWrite(LCD_DATAH_BASE, 0xFF, d);
}

void LCD_init_periph(void)
{
	// Init PWM for backlight
	SysCtlPeripheralEnable(SYSCTL_PERIPH_PWM);
	SysCtlPeripheralReset(SYSCTL_PERIPH_PWM);
//	PWMGenConfigure(PWM1_BASE, PWM_GEN_2,
//			PWM_GEN_MODE_DOWN | PWM_GEN_MODE_NO_SYNC);

	return;
}

void LCD_init_pins(void)
{
	//GPIO D: LCD DATA 0-7
	SysCtlPeripheralEnable(LCD_DATAL_PERIPH);
	GPIOPinTypeGPIOOutput(LCD_DATAL_BASE, 0xFF);
	GPIOPinWrite(LCD_DATAL_BASE, 0xFF, 0x0); // All low.

	//GPIO E: LCD DATA 8-15
	SysCtlPeripheralEnable(LCD_DATAH_PERIPH);
	GPIOPinTypeGPIOOutput(LCD_DATAH_BASE, 0xFF);
	GPIOPinWrite(LCD_DATAH_BASE, 0xFF, 0x0); // All low.

	//GPIO F: LCD_RST, LCD_BL
	SysCtlPeripheralEnable(LCD_RST_PERIPH);
	GPIOPinTypeGPIOOutput(LCD_RST_BASE, LCD_RST_PIN);
	GPIOPinWrite(LCD_RST_BASE, LCD_RST_PIN, LCD_RST_PIN); // RST = 1

	//Commands
	SysCtlPeripheralEnable(LCD_COMMAND_PERIPH);
	GPIOPinTypeGPIOOutput(LCD_COMMAND_BASE,
			LCD_RD_PIN | LCD_WR_PIN | LCD_CS_PIN | LCD_RS_PIN);
	GPIOPadConfigSet(LCD_COMMAND_BASE, LCD_RD_PIN, GPIO_STRENGTH_4MA,
			GPIO_PIN_TYPE_STD_WPU);
	GPIOPinWrite(LCD_COMMAND_BASE,
			LCD_RD_PIN | LCD_WR_PIN | LCD_CS_PIN | LCD_RS_PIN,
			LCD_RD_PIN | LCD_WR_PIN | LCD_CS_PIN | LCD_RS_PIN);

	return;
}


void ssd1289_init()
{
	LCD_init_periph();
	LCD_init_pins();
	// Reset
	GPIOPinWrite(LCD_RST_BASE,LCD_RST_PIN,0);
	GPIOPinWrite(LCD_RST_BASE,LCD_RST_PIN,LCD_RST_PIN);


	// CS low
	GPIOPinWrite(LCD_CS_BASE,LCD_CS_PIN,0);
	
	/* OSCEN=1 */
	ssd1289_send(SSD1289_REG_OSCILLATION, 0x0001);

	/* DCT=b1010=fosc/4 BT=b001=VGH:6, VGL:-4 */
	/* DC=b1010=fosc/4 AP=b010=small to medium */
	ssd1289_send(SSD1289_REG_POWER_CTRL_1, 0xa2a4);

	/* VRC=b100:5.5V */
	ssd1289_send(SSD1289_REG_POWER_CTRL_2, 0x0004);

	/* VRH=b1000:Vref*2.165 */
	ssd1289_send(SSD1289_REG_POWER_CTRL_3, 0x0308);

	/* VCOMG=1 VDV=b1000:VLCD63*1.05 */
	ssd1289_send(SSD1289_REG_POWER_CTRL_4, 0x3000);

	/* nOTP=1 VCM=0x2a:VLCD63*0.77 */
	ssd1289_send(SSD1289_REG_POWER_CTRL_5, 0x00ea);

	/* RL=0 REV=1 CAD=0 BGR=1 SM=0 TB=1 MUX=0x13f=319 */
	ssd1289_send(SSD1289_REG_DRIVER_OUT_CTRL, 0x2b3f);

	/* FLD=0 ENWS=0 D/C=1 EOR=1 WSMD=0 NW=0x00=0 */
	ssd1289_send(SSD1289_REG_LCD_DRIVE_AC, 0x0600);

	/* SLP=0 */
	ssd1289_send(SSD1289_REG_SLEEP_MODE, 0x0000);

	/* VSMode=0 DFM=3:65k TRAMS=0 OEDef=0 WMode=0 Dmode=0 */
	/* TY=0 ID=2 AM=1 LG=0(orig: ID=3, AM=0) */

	/* ssd1289_send(SSD1289_REG_ENTRY_MODE, 0x6030); */
	ssd1289_send(SSD1289_REG_ENTRY_MODE, 0x6028);

	/* PT=0 VLE=1 SPT=0 GON=1 DTE=1 CM=0 D=3 */
	ssd1289_send(SSD1289_REG_DISPLAY_CTRL, 0x0233);

	/* NO=0 SDT=0 EQ=0 DIV=0 SDIV=1 SRTN=1 RTN=9:25 clock */
	ssd1289_send(SSD1289_REG_FRAME_CYCLE, 0x0039);

	/* SCN=0 */
	ssd1289_send(SSD1289_REG_GATE_SCAN_START, 0x0000);

	/* PKP1=7 PKP0=7 */
	ssd1289_send(SSD1289_REG_GAMMA_CTRL_1, 0x0707);

	/* PKP3=2 PKP2=4 */
	ssd1289_send(SSD1289_REG_GAMME_CTRL_2, 0x0204);

	/* PKP5=2 PKP4=2 */
	ssd1289_send(SSD1289_REG_GAMMA_CTRL_3, 0x0204);

	/* PRP1=5 PRP0=2 */
	ssd1289_send(SSD1289_REG_GAMMA_CTRL_4, 0x0502);

	/* PKN1=5 PKN0=7 */
	ssd1289_send(SSD1289_REG_GAMMA_CTRL_5, 0x0507);

	/* PKN3=2 PNN2=4 */
	ssd1289_send(SSD1289_REG_GAMMA_CTRL_6, 0x0204);

	/* PKN5=2 PKN4=4 */
	ssd1289_send(SSD1289_REG_GAMMA_CTRL_7, 0x0204);

	/* PRN1=5 PRN0=2 */
	ssd1289_send(SSD1289_REG_GAMMA_CTRL_8, 0x0502);

	/* VRP1=3 VRP0=2 */
	ssd1289_send(SSD1289_REG_GAMMA_CTRL_9, 0x0302);

	/* VRN1=3 VRN0=2 */
	ssd1289_send(SSD1289_REG_GAMMA_CTRL_10, 0x0302);

	/* WMR=0 WMG=0 */
	ssd1289_send(SSD1289_REG_WR_DATA_MASK_1, 0x0000);

	/* WMB=0 */
	ssd1289_send(SSD1289_REG_WR_DATA_MASK_2, 0x0000);

	/* OSC=b1010:548k */
	ssd1289_send(SSD1289_REG_FRAME_FREQUENCY, 0xa000);

	/* SS1=0 */
	ssd1289_send(SSD1289_REG_FIRST_WIN_START, 0x0000);

	/* SE1=319 */
	ssd1289_send(SSD1289_REG_FIRST_WIN_END,(SSD1289_WIDTH - 1));

	/* SS2=0 */
	ssd1289_send(SSD1289_REG_SECND_WIN_START, 0x0000);

	/* SE2=0 */
	ssd1289_send(SSD1289_REG_SECND_WIN_END, 0x0000);

	/* VL1=0 */
	ssd1289_send(SSD1289_REG_V_SCROLL_CTRL_1, 0x0000);

	/* VL2=0 */
	ssd1289_send(SSD1289_REG_V_SCROLL_CTRL_2, 0x0000);

	/* HEA=0xef=239 HSA=0 */
	ssd1289_send(SSD1289_REG_H_RAM_ADR_POS,(SSD1289_HEIGHT - 1) << 8);

	/* VSA=0 */
	ssd1289_send(SSD1289_REG_V_RAM_ADR_START, 0x0000);

	/* VEA=0x13f=319 */
	ssd1289_send(SSD1289_REG_V_RAM_ADR_END,(SSD1289_WIDTH - 1));

	ssd1289_fill(0);
	// CS high
	//GPIOPinWrite(LCD_CS_BASE,LCD_CS_PIN,LCD_CS_PIN);
}

void ssd1289_send(uint16_t cmd, uint16_t data)
{
	ssd1289_send_cmd(cmd);
	ssd1289_send_data(data);
}

void ssd1289_send_cmd(uint16_t raw)
{
	/* sending cmd over parallel 16bit line */
	GPIOPinWrite(LCD_RS_BASE,LCD_RS_PIN,0);
	ssd1289_write(raw);
}

void ssd1289_send_data(uint16_t raw)
{
	/* sending data */
	GPIOPinWrite(LCD_RS_BASE,LCD_RS_PIN,LCD_RS_PIN);
	ssd1289_write(raw);
}

static inline void ssd1289_write(uint16_t raw)
{
	WriteLow(raw);
	WriteHigh(raw >> 8);

	GPIOPinWrite(LCD_COMMAND_BASE,LCD_WR_PIN,0);
	GPIOPinWrite(LCD_COMMAND_BASE,LCD_WR_PIN,LCD_WR_PIN);
}

void ssd1289_fill(color_t color)
{
	uint16_t i, j;
	
	ssd1289_send(0x0022,  color);
	
	for(i=0; i < SSD1289_HEIGHT; i++)
	{
		for(j=0; j < SSD1289_WIDTH; j++)
		{
			GPIOPinWrite(LCD_COMMAND_BASE,LCD_WR_PIN,0);
			GPIOPinWrite(LCD_COMMAND_BASE,LCD_WR_PIN,LCD_WR_PIN);
		}
	}
}

