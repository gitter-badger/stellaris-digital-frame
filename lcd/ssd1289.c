/* http://apbteam.org/ */
void
ssd1289_circle(color_t color, yaxis_t y, xaxis_t x, radius_t r)
{
	int tx = 0;
	int ty = r;
	int m = 5 - (4 * r);
	
	while(tx <= ty)
	{
		ssd1289_pixel(color, ty + y, tx + x);
		ssd1289_pixel(color, tx + y, ty + x);
		ssd1289_pixel(color, ty + y, -tx + x);
		ssd1289_pixel(color, tx + y, -ty + x);
		ssd1289_pixel(color, -ty + y, tx + x);
		ssd1289_pixel(color, -tx + y, ty + x);	
		ssd1289_pixel(color, -ty + y, -tx + x);
		ssd1289_pixel(color, -tx + y, -ty + x);
	
		if(m > 0)
		{
			ty--;
			m -= (8 * ty);
		}
		
		tx++;
		m += (8 * tx) + 4;
	}
}

void
ssd1289_pixel(color_t color, yaxis_t y, xaxis_t x)
{
	/* fall in between screen */
	if (x < SSD1289_WIDTH && y < SSD1289_HEIGHT)
	{
		ssd1289_set_cursor(y, x);
		ssd1289_send(0x0022, color);
	}
}

